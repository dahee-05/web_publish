// 단항 연산자 : ++(1씩 증가), --(1씩 감소), !(=not), !!

let a = 10;
console.log(++a); // 11
console.log(a++); // 11출력 후 1증가 
console.log(a);   // 12

let b = 10 ;
console.log(--b); // 9
console.log(b--); // 9출력 후 -1 감소 
console.log(b);   // 8


let flag = true; 
console.log(flag);
console.log(!flag);
console.log(!!flag); // not(!flag)









