/* 
* 변수 선언 : var, let, const
* 형식 : (var, let, const) 변수명 ; < 변수선언 
* 데이터 초기화 (var, let, const) 변수명 = 데이터 값; 
* let,const는 선언뿐만 아니라 데이터 초기화까지 해야함 
* let 은 변수 선언은 한번만 가능하고 값 재할당은 가능하다 
*/

let name = "홍길동";
let age = 20;

name = "백종원";
age = '서울시';
age = 30;

const adress = "서울시";

console.log(name);
console.log(adress);
console.log(age);




